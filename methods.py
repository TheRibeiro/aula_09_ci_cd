def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

# ------------- THE BASIC OPERATIONS ------------

def math_sum(num1, num2):
    result = (num1 + num2)
    return result

def math_subtraction(num1, num2):
    result = (num1 - num2)
    return result

def math_multiplication(num1, num2):
    result = (num1 * num2)
    return result

def math_division(num1, num2):
    result = (num1 / num2)
    return result





